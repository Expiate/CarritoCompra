package mainPackage.carritocompra;

public class objetosListasDeCompra {
    private String titulo;
    private String desc;

    public objetosListasDeCompra(String titulo, String desc) {
        this.titulo = titulo;
        this.desc = desc;
    }

    public String getTitulo() {
        return titulo;
    }

    public String getDesc() {
        return desc;
    }
}
